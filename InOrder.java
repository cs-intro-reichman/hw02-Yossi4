import java.util.*;
public class InOrder {
	public static void main (String[] args) {
		
		int last = -1;
		int current = (int) (Math.random() * 10);

		

		while (current>last){
				System.out.print(current);
				System.out.print(" ");
				last = current;
				current = (int) (Math.random() * 10);


		}




			
		





	}
}
